package DataTypes;

public class DataTypes {

	public static void main(String[] args) {

		int x = 10;
		while (x >= 1) {
			System.out.println(x);
			x--;
		}
		System.out.println("-----------------");
		for (int y = 10; y >= 1; y--) {
			System.out.println(y);
		}
		System.out.println("-----------------");
		int z = 10;
		do {
			System.out.println(z);
			z--;
		} while (z >= 1);

	}

}
